# Write a small program to calculate how many minutes there are in a year. 
# Feel free to google the formula if you need help getting started.

days_in_year = 365
hours_in_a_day = 24
minutes_in_an_hour = 60
minutes_in_a_year = minutes_in_an_hour * hours_in_a_day * days_in_year
puts minutes_in_a_year