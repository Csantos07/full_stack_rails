# Data Types
# ----------
#
# Data Types are a particular type of a data item. 
# Data can represent:

# -------------------------------------------------------------------------
# Numbers  | Numeric values that you can perform mathematical operations on
# -------------------------------------------------------------------------
# Strings  | A sequence of characters representing text or information
# -------------------------------------------------------------------------
# Symbols  | Unique identifiers
# -------------------------------------------------------------------------
# Booleans | true/false values
# -------------------------------------------------------------------------
# Arrays   | An integer indexed collection
# -------------------------------------------------------------------------
# Hashes   | An integer indexed collection
# -------------------------------------------------------------------------
# Objects  | An integer indexed collection
# -------------------------------------------------------------------------
# Null     | The absence of a value
# -------------------------------------------------------------------------